    static public ArrayList<News> categoryNews(final int category) {
        final String[] pool = ids(category);
        if (pool == null) return null;
        int n = 20;
        final ArrayList<News> newslist = new ArrayList<>();
        ArrayList<Thread> threadpool = new ArrayList<>();
        for (int i=0; i<n; ++i) {
            final int _i = i;
            Thread t = new Thread(new Runnable() {
                @Override
                public void run() {
                    String url = "https://covid-dashboard-api.aminer.cn/event/" + pool[(cnt[category]+_i)%pool.length];
                    try (InputStream is = new URL(url).openStream()) {
                        BufferedReader rd = new BufferedReader(new InputStreamReader(is, Charset.forName("UTF-8")));
                        JSONObject data = new JSONObject(readAll(rd)).getJSONObject("data");
                        final News news = new News(
                                data.getString("title"),
                                data.getString("time"),
                                "Source: unknown",
                                getString(data.getJSONArray("urls"),0),
                                "The quick brown fox jumps over a lazy dog.",
                                new String[]{"http://p5.itc.cn/q_70/images03/20200807/9e87c806515a41aeb0ba94eae6bfdb30.png"},
                                false,
                                ""
                        );
                        synchronized (newslist) {
                            newslist.add(news);
                        }
                    }
                    catch (Exception e) {
                        e.printStackTrace();
                    }
                }
            });
            t.start();
            threadpool.add(t);
        }
        // wait for jobs to finish
        for (Thread t: threadpool) {
            try {
                t.join();
            } catch (InterruptedException e) {
                e.printStackTrace();
            }
        }
        cnt[category] += n;
        return newslist;
    }
